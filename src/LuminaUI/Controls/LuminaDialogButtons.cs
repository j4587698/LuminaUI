namespace LuminaUI.Controls;

public enum LuminaDialogButtons
{
    /// <summary>
    /// Displays only an OK button.
    /// </summary>
    Ok,

    /// <summary>
    /// Displays OK and Cancel buttons.
    /// </summary>
    OkCancel,

    /// <summary>
    /// Displays Yes and No buttons.
    /// </summary>
    YesNo,

    /// <summary>
    /// Displays Yes, No, and Cancel buttons.
    /// </summary>
    YesNoCancel,

    /// <summary>
    /// Indicates that the dialog uses custom buttons defined by the user.
    /// </summary>
    Custom
}
